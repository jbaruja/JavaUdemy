
package br.com.jorge.Livro.OperadoresRelacionais;

/**
 *
 * @author home
 */
public class OperadoresRelacionais {
    public static void main(String[] args){
        int a = 15;
        int b = 12;
        System.out.println("a = " +a);
        System.out.println("b = " +b);
        System.out.println("a == b: " + (a==b) + " <- == Igualdade: retorna true se ambos valores forem iguais");
        System.out.println("a != b: " + (a!=b) + " <- != Diferença: retorna true se ambos valores são diferentes");
        System.out.println("a < b: " + (a<b));
        System.out.println("a > b: " + (a>b));
        System.out.println("a <= b: " + (a<=b));
        System.out.println("a >=b: " + (a>=b));
    }
}
