/**
 * Atribuir um valor a uma variável e imprimir Atribuir um laço determinando as
 * x de rep
 *
 * @author home
 */
public class OperDeAtribuicao {

    @SuppressWarnings("empty-statement")
    public static void main(String[] args) {
        int i = 0;
        int y = i + 2;
        int r = i + 1;
        for (int a = 1; a < 10; a++) {
            System.out.println("Valor do a: " + a);
        }
        System.out.println("Valor do i: " + i);
        System.out.println("Valor do y: " + y);
        System.out.println("Valor do r: " + r);

    }
}
