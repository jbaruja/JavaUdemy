/**
 * criar um laço for até 20 indicando o número par e impar
 * Reagir para um rango de dados de 5 a 7
 *
 * @author home
 */
public class DesvioDeFluxoIfElse {
    public static void main(String[]args){
        int x = 1;
        if(x ==1){
            System.out.println("Ok! na primeira!");
        }else if(x ==2){
            System.out.println("Acertou na segunda!");
        }else{
            System.out.println("Não foi dessa vez..");
        }
    }
}
    




